//*1.Slicing String : Same as Array
const name = "Rahat Karim Jisan";
console.log(name.slice(3, 6)); //O/p: at

//*2. 1 parameter
const name = "Rahat Karim Jisan";
console.log(name.slice(3)); //O/p: at Karim Jisan

//*2. minus parameter
const name = "Rahat Karim Jisan";
console.log(name.slice(-3)); //O/p: san
